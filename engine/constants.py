# Board Coordinate Mappings (ACF Notation)
ACF_TO_COORD, COORD_TO_ACF = {}, {}
num = 1
for r in range(8):
    for c in range(8):
        if (r + c) % 2 == 1:
            ACF_TO_COORD[num], COORD_TO_ACF[(r, c)] = (r, c), num
            num += 1

def coord_to_acf_notation(coord):
    return str(COORD_TO_ACF.get(coord, "??"))

